function getWHOCurves(gender, type, agesArray) {
    if (!gender) gender = 'male';
    if (gender === 'other') gender = 'male'; 
    const data = WHO_PERCENTILES[gender][type];
    
    const p5 = [], p50 = [], p95 = [];
    agesArray.forEach(age => {
        let a = Math.round(age);
        if (a < 2) a = 2;
        if (a > 18) a = 18;
        const vals = data[a] || data[10];
        p5.push(vals[0]);
        p50.push(vals[1]);
        p95.push(vals[2]);
    });
    
    return { p5, p50, p95 };
}
